export function getFrenchPokemonId(frenchName: string): number | null {
  const normalizedName = frenchName.toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '') // Supprimer les accents
    .replace(/[^a-z0-9]/g, '')
  
  return FRENCH_NAMES[normalizedName] || null
}

export function searchFrenchNames(query: string): number[] {
  const normalizedQuery = query.toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]/g, '')
  
  if (normalizedQuery.length < 2) return []
  
  const results: number[] = []
  
  for (const [frenchName, id] of Object.entries(FRENCH_NAMES)) {
    if (frenchName.includes(normalizedQuery)) {
      results.push(id)
      if (results.length >= 10) break // Limiter à 10 résultats
    }
  }
  
  return results
}